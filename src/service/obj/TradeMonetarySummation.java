package service.obj;

public class TradeMonetarySummation {

	public TradeMonetarySummation() {
		
	}

	private Double originalAmount;
	private Double lineTotalAmount;
	private Double differenceAmount;
	private Double chargeAmount;
	private Double allowanceAmount;
	private Double taxTotalAmount;
	private Double grandTotalAmount;
	/**
	 * @return the originalAmount
	 */
	public Double getOriginalAmount() {
		return originalAmount;
	}
	/**
	 * @param originalAmount the originalAmount to set
	 */
	public void setOriginalAmount(Double originalAmount) {
		this.originalAmount = originalAmount;
	}
	/**
	 * @return the lineTotalAmount
	 */
	public Double getLineTotalAmount() {
		return lineTotalAmount;
	}
	/**
	 * @param lineTotalAmount the lineTotalAmount to set
	 */
	public void setLineTotalAmount(Double lineTotalAmount) {
		this.lineTotalAmount = lineTotalAmount;
	}
	/**
	 * @return the differenceAmount
	 */
	public Double getDifferenceAmount() {
		return differenceAmount;
	}
	/**
	 * @param differenceAmount the differenceAmount to set
	 */
	public void setDifferenceAmount(Double differenceAmount) {
		this.differenceAmount = differenceAmount;
	}
	/**
	 * @return the chargeAmount
	 */
	public Double getChargeAmount() {
		return chargeAmount;
	}
	/**
	 * @param chargeAmount the chargeAmount to set
	 */
	public void setChargeAmount(Double chargeAmount) {
		this.chargeAmount = chargeAmount;
	}
	/**
	 * @return the allowanceAmount
	 */
	public Double getAllowanceAmount() {
		return allowanceAmount;
	}
	/**
	 * @param allowanceAmount the allowanceAmount to set
	 */
	public void setAllowanceAmount(Double allowanceAmount) {
		this.allowanceAmount = allowanceAmount;
	}
	/**
	 * @return the taxTotalAmount
	 */
	public Double getTaxTotalAmount() {
		return taxTotalAmount;
	}
	/**
	 * @param taxTotalAmount the taxTotalAmount to set
	 */
	public void setTaxTotalAmount(Double taxTotalAmount) {
		this.taxTotalAmount = taxTotalAmount;
	}
	/**
	 * @return the grandTotalAmount
	 */
	public Double getGrandTotalAmount() {
		return grandTotalAmount;
	}
	/**
	 * @param grandTotalAmount the grandTotalAmount to set
	 */
	public void setGrandTotalAmount(Double grandTotalAmount) {
		this.grandTotalAmount = grandTotalAmount;
	}
}
